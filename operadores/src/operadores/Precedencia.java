package operadores;

public class Precedencia {

    public static void main(String[] args) {
        double Nota_Estu1 = 3.5;
        double Nota_Estu2= 3.6;
        double Nota_Estu3=5.0;
        double Resultado1=(Nota_Estu1 + Nota_Estu2 + Nota_Estu3)/3;
        
       System.out.println("El promedio es:"+Resultado1);
       
       
       int Estu_Sede1=3;
       int Estu_Sede2=4;
       int Estu_Sede3=2;
       int Resultado2 = (Estu_Sede1 + Estu_Sede2 + Estu_Sede3)*3;
       
       System.out.println("Resultado de entrega de boletas es:" +Resultado2);
    }
    
}


